//Pag 19, Actvs 1. Tabla de Multiplacar incremental y bucle hasta 10 
 public class Actividad5 {
    public static void main(String[] args) {
        // //Bucle anidado, tabla de multiplicar
         for(int i=1;i<=10;i++){
             for(int j=1;j<=10;j++){
                 System.out.println(i+" x "+j+" = "+(i*j));
             }
        System.out.println(""); 
         }
    }
}

